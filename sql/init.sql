CREATE TABLE port
(
  id SERIAL PRIMARY KEY,
  name VARCHAR NOT NULL,
  unlocode VARCHAR NOT NULL UNIQUE
);
CREATE UNIQUE INDEX unlocode_idx ON port (unlocode);

CREATE TABLE terminal
(
  id SERIAL PRIMARY KEY,
  name VARCHAR NOT NULL,
  firms_code VARCHAR,
  port_id INT NOT NULL REFERENCES port (id)
);

CREATE TABLE vessel
(
  id SERIAL PRIMARY KEY,
  name VARCHAR NOT NULL,
  imo INT NOT NULL,
  terminal_id INT REFERENCES terminal (id)
);

CREATE TABLE container
(
  id SERIAL PRIMARY KEY,
  container_id VARCHAR NOT NULL,
  vessel_id INT REFERENCES vessel (id)
);

